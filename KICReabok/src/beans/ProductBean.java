package beans;

public class ProductBean {
	
	private String pdcode;
	private String p_name;
	private String p_price;
	private String p_color;
	private String p_page;
	private String p_classify;
	private String p_explain;
	private String p_img;
	
	public String getPdcode() {
		return pdcode;
	}
	public void setPdcode(String pdcode) {
		this.pdcode = pdcode;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_price() {
		return p_price;
	}
	public void setP_price(String p_price) {
		this.p_price = p_price;
	}
	public String getP_color() {
		return p_color;
	}
	public void setP_color(String p_color) {
		this.p_color = p_color;
	}
	public String getP_page() {
		return p_page;
	}
	public void setP_page(String p_page) {
		this.p_page = p_page;
	}
	public String getP_classify() {
		return p_classify;
	}
	public void setP_classify(String p_classify) {
		this.p_classify = p_classify;
	}
	public String getP_explain() {
		return p_explain;
	}
	public void setP_explain(String p_explain) {
		this.p_explain = p_explain;
	}
	public String getP_img() {
		return p_img;
	}
	public void setP_img(String p_img) {
		this.p_img = p_img;
	}
	
}